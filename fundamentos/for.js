//estruturas de repetições determinadas FOR e WHILE

console.log('estrutura de repetição determinada FOR')
for(let contadorFor = 0; contadorFor <= 10; contadorFor ++){
      console.log(`contador for = ${contadorFor}`)
}

console.log('estrutura de repetição determinada while' )
contadorWhile = 1
while(contadorWhile <= 10){
   console.log(`contador while ${contadorWhile}`)
   contadorWhile ++
}




const notas = [7.8, 8.5, 8.6, 9.2, 0.3]
