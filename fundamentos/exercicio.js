let letra = ""

function numeroLetra(nomeLetra) {
    switch (letra) {
        case 'o':
            nomeLetra = '0'
            break;
        case 'i':
            nomeLetra = '1'
            break;
        case 'e':
            nomeLetra = '3'
            break;
        case 'a':
            nomeLetra = '4'
            break;
        case 's':
            nomeLetra = '5'
            break;
        case 'g':
            nomeLetra = '6'
            break;
        case 't':
            nomeLetra = '7'

    }
}
console.log(numeroLetra('vitor'))